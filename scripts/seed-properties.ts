async function seed() {
  console.log(`Seeding ${properties.length} properties...`);

  const transaction = client.transaction();

  for (const prop of properties) {
    transaction.createOrReplace(prop);
    console.log(
      `  + ${prop.name} (${prop.category}) - ${prop.rooms.length} rooms`,
    );
  }

  const result = await transaction.commit();
  console.log(`\nDone! ${result.documentIds.length} documents created.`);

  // Summary
  const totalRooms = properties.reduce((sum, p) => sum + p.rooms.length, 0);
  const villas = properties.filter((p) => p.category === "villa");
  const hotels = properties.filter((p) => p.category === "hotel");
  console.log(`\nSummary:`);
  console.log(
    `  Villa: ${villas.length} properties, ${villas.reduce((s, p) => s + p.rooms.length, 0)} rooms`,
  );
  console.log(
    `  Hotel: ${hotels.length} properties, ${hotels.reduce((s, p) => s + p.rooms.length, 0)} rooms`,
  );
  console.log(`  Total: ${properties.length} properties, ${totalRooms} rooms`);
}

