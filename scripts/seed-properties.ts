function toSlug(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/đ/g, "d")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function parseViewType(
  category: string,
): "sea-view" | "mountain-view" | "no-view" {
  if (category.includes("View biển")) return "sea-view";
  if (category.includes("View núi")) return "mountain-view";
  return "no-view";
}

